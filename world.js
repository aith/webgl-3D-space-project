function drawCube(gl, n, indexBuffer, a_Position, translation, rotation, scale){

    gl.bindBuffer(gl.ARRAY_BUFFER, vertexBuffer);  
    //before doing the following, have the matrix calculations complete
    gl.bufferData(gl.ARRAY_BUFFER, vertices, gl.STATIC_DRAW);

    //vertices
    //break this off into setTexCoords later
    gl.vertexAttribPointer(a_Position, 3, gl.FLOAT, false, FSIZE * 3, 0);

    gl.enableVertexAttribArray(a_Position);

    gl.uniformMatrix4fv(u_RotationMatrix, false, rotation.elements);
    gl.uniformMatrix4fv(u_TranslationMatrix, false, translation.elements);
    gl.uniformMatrix4fv(u_ScaleMatrix, false, scale.elements);

    gl.bindBuffer(gl.ELEMENT_ARRAY_BUFFER, indexBuffer);
    gl.bufferData(gl.ELEMENT_ARRAY_BUFFER, indices, gl.STATIC_DRAW);
    gl.drawElements(gl.TRIANGLES, n, gl.UNSIGNED_BYTE, 0);
}

