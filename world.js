var counterThatFlipsSign = 0;
var hasCounterHitMax = -1;
var counterMax = 360;
//speeds up animation loop
var counterScalar = counterMax/60;
function updateFrameCounter(){
  //increase counter relative to PC's run speed
  //~360 per sec
  if(hasCounterHitMax != 1){
    if(counter < counterMax){
      counter += counterScalar;
      if(counterThatFlipsSign < 354){
        counterThatFlipsSign += counterScalar;
      } else counterThatFlipsSign = 0;
    } 
    else {
      hasCounterHitMax = 1;
      counter -= counterScalar;
      counterThatFlipsSign = -354;
    }
  } else {
    if(counter > 0){
      counter -= counterScalar;
      counterThatFlipsSign += counterScalar;
    } 
    else {
      //reset after counter minimum is reached
      hasCounterHitMax = -1;
      counter += counterScalar;
      counterThatFlipsSign += counterScalar;
    }
  }
}

function updateCamera(){
  ViewMatrix.setLookAt(eyeX, eyeY, eyeZ, atX, atY, atZ, upX, upY, upZ);
}

